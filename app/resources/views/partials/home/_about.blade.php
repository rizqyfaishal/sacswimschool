                <span class="stripped-center blue small"></span>
                <p>Menginspirasi setiap individu melalui renang dan menciptakan iklim pembelajaran yang
                    kreatif, peduli, inovatif serta melahirkan generasi atlet yang mandiri, sportif, dan kompetitif.</p>
                <h4 class="text-center">Misi</h4>
                <span class="stripped-center blue small"></span>
                <ul>
                    <li>Memberikan fasilitas dan pelayanan berkualitas yang didukung oleh program berstandar
                        Internasional.</li>
                    <li>Menciptakan media pembelajaran yang disiplin, kondusif, dan semangat menjiwai
                        renang untuk mengoptimalkan performa terbaiknya.</li>
